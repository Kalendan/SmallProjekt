public class Util {


    public static double averageVelue1(double maxSalary, double minSalary) {
        return (maxSalary + minSalary) / 2;
    }
    public static double averageVelue2(double maxSalary, double minSalary, int constantvelue) {
        return (maxSalary + minSalary + constantvelue);
    }
}
